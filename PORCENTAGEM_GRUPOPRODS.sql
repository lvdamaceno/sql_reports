DECLARE @YEAR int
SET @YEAR=2017

DECLARE @MONTH int
SET @MONTH=3

SELECT
VEN.CODEMP
,ITE.CODVEND
,VEN.APELIDO
--,[ELETRO] = SUM(CASE WHEN (CODGRUPOPROD>='01010000' AND CODGRUPOPROD<='01029999') OR 
--                          (CODGRUPOPROD>='01040000' AND CODGRUPOPROD<='01049999') THEN ITE.QTDNEG END) 
                          
,[%ELETRO] = 
CAST( 
  (100 * 
    SUM(CASE WHEN (CODGRUPOPROD>='01010000' AND CODGRUPOPROD<='01029999') OR 
    (CODGRUPOPROD>='01040000' AND CODGRUPOPROD<='01049999') THEN ITE.QTDNEG END) 
  ) / 
  (
    (SUM(CASE WHEN (CODGRUPOPROD>='01010000' AND CODGRUPOPROD<='01029999') OR 
    (CODGRUPOPROD>='01040000' AND CODGRUPOPROD<='01049999') THEN ITE.QTDNEG END) 
  )+
    (SUM(CASE WHEN (CODGRUPOPROD>='01030000' AND CODGRUPOPROD<='01039999') OR
    (CODGRUPOPROD>='01050000' AND CODGRUPOPROD<='01999999') THEN ITE.QTDNEG END))
  ) 
AS NUMERIC(15,2))
                          
--,[MOVEIS] = SUM(CASE WHEN (CODGRUPOPROD>='01030000' AND CODGRUPOPROD<='01039999') OR
--                          (CODGRUPOPROD>='01050000' AND CODGRUPOPROD<='01999999') THEN ITE.QTDNEG END) 
                          
,[%MOVEIS] = 
CAST( 
  (100 * 
    SUM(CASE WHEN (CODGRUPOPROD>='01030000' AND CODGRUPOPROD<='01039999') OR
    (CODGRUPOPROD>='01050000' AND CODGRUPOPROD<='01999999') THEN ITE.QTDNEG END)
  ) / 
  (
    (SUM(CASE WHEN (CODGRUPOPROD>='01010000' AND CODGRUPOPROD<='01029999') OR 
    (CODGRUPOPROD>='01040000' AND CODGRUPOPROD<='01049999') THEN ITE.QTDNEG END) 
  )+
    (SUM(CASE WHEN (CODGRUPOPROD>='01030000' AND CODGRUPOPROD<='01039999') OR
    (CODGRUPOPROD>='01050000' AND CODGRUPOPROD<='01999999') THEN ITE.QTDNEG END))
  )
AS NUMERIC(15,2))

FROM
TGFCAB CAB
,TGFITE ITE
,TGFPRO PRO
,TGFVEN VEN

WHERE
CAB.NUNOTA = ITE.NUNOTA
AND PRO.CODPROD = ITE.CODPROD
AND ITE.CODVEND = VEN.CODVEND
AND YEAR(CAB.DTNEG) = @YEAR
AND MONTH(CAB.DTNEG) = @MONTH
AND CAB.CODTIPOPER IN (1110,1101,1112)
AND ITE.CODVEND NOT IN (0,22,202)

GROUP BY
VEN.CODEMP
,ITE.CODVEND
,VEN.APELIDO

ORDER BY
VEN.CODEMP
,ITE.CODVEND
