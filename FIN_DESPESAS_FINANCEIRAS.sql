SELECT
CAB.CODPARC [COD]
,RTRIM(PAR.NOMEPARC) [FORNECEDOR]
,CAB.NUMNOTA [TITULO]
,TIP.DESCROPER [TOP]
,LTRIM(CAB.CODEMP) [LJ]
,FIN.DTNEG [DT.NEG.]
,FIN.DTVENC [DT.VENC.]
,FIN.DESDOBRAMENTO [DESDOB.]
,FIN.VLRDESDOB [VLR.DESDOB.]
,CASE
    WHEN FIN.DHBAIXA IS NULL THEN 'ABERTO'
    WHEN FIN.DHBAIXA IS NOT NULL THEN 'BAIXADO'
END AS [SITUACAO]
FROM
TGFCAB CAB
,TGFFIN FIN
,TGFPAR PAR
,VIEWTOPS TIP

WHERE

CAB.NUNOTA =FIN.NUNOTA
AND CAB.CODPARC=PAR.CODPARC
AND CAB.CODTIPOPER = TIP.CODTIPOPER
AND CAB.CODTIPOPER IN (1401,1402,1403,1404,1405,1408,1409,1410,1411,1412,1413,1414,1415,1417,1418,1419,
                       1429,1431,1432,1433,1434,1436,1437,1438,1450,1452,1453)
AND FIN.DTVENC >= :DATAINI
AND FIN.DTVENC <= :DATAFIM 
